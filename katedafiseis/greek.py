_WORD_RE = re.compile(r"[^\s/\-.,()«»\"']+")


def _fix_word(w):
    hit = WORD_ACCENTS.get(normalize(w))
    if hit:
        return hit
    if w[0].isdigit():            # «28ΗΣ» -> «28ης»
        out = w.lower()
    elif len(w) == 1:
        return w                  # αρχικά («Β ΠΑΡΟΔΟΣ»)
    else:
        out = w[0].upper() + w[1:].lower()
    if out.endswith("σ"):
        out = out[:-1] + "ς"
    return out


def greek_title(s):
    """«ΝΕΑ ΑΜΙΣΟΣ» -> «Νέα Αμισός»· άγνωστες λέξεις χωρίς τόνο."""
    if not s:
        return s
    return _WORD_RE.sub(lambda m: _fix_word(m.group(0)), s)


def dimos_display(kallikratis_label):
    """«ΔΗΜΟΣ ΔΡΑΜΑΣ» -> «Δήμος Δράμας» (πλήρες τονισμένο όνομα)."""
    return DIMOI.get(kallikratis_label) or greek_title(kallikratis_label)


def pretty_area(label):
    """Περιγραφή περιοχής του resolve_area σε κανονική γραφή."""
    parts = [p.strip() for p in label.split(",")]
    return ", ".join(DIMOI.get(p) or greek_title(p) for p in parts)
